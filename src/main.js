let currentLang = 'en'

function applyLang(lang) {
  currentLang = lang
  const dict = STRINGS[lang]
  document.documentElement.lang = lang

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n')
    if (dict[key]) el.textContent = dict[key]
  })

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('is-active', btn.getAttribute('data-lang') === lang)
  })

  try {
    localStorage.setItem('fensterpro-lang', lang)
  } catch {
    /* ignore */
  }
}

function initLang() {
  try {
    const saved = localStorage.getItem('fensterpro-lang')
    if (saved === 'en' || saved === 'de') {
      applyLang(saved)
      return
    }
  } catch {
    /* ignore */
  }
  applyLang('de')
}

document.querySelectorAll('.lang-btn').forEach((btn) => {
  btn.addEventListener('click', () => {
    applyLang(btn.getAttribute('data-lang'))
  })
})

const nav = document.querySelector('.nav')
const navToggle = document.querySelector('.nav-toggle')
const navMenu = document.getElementById('nav-menu')

if (navToggle && nav) {
  navToggle.addEventListener('click', () => {
    const open = !nav.classList.contains('is-open')
    nav.classList.toggle('is-open', open)
    navToggle.setAttribute('aria-expanded', open)
  })

  navMenu?.querySelectorAll('a').forEach((a) => {
    a.addEventListener('click', () => {
      nav.classList.remove('is-open')
      navToggle.setAttribute('aria-expanded', 'false')
    })
  })
}

const form = document.getElementById('contact-form')
const toast = document.getElementById('toast')
const sentRequests = document.getElementById('sent-requests')

function showToast(message) {
  if (!toast) return
  toast.textContent = message
  toast.hidden = false
  toast.classList.add('is-visible')
  window.setTimeout(() => {
    toast.classList.remove('is-visible')
    window.setTimeout(() => {
      toast.hidden = true
    }, 400)
  }, 3200)
}

function loadSent() {
  try {
    const raw = localStorage.getItem('fensterpro-sent')
    const parsed = raw ? JSON.parse(raw) : []
    return Array.isArray(parsed) ? parsed : []
  } catch {
    return []
  }
}

function saveSent(items) {
  try {
    localStorage.setItem('fensterpro-sent', JSON.stringify(items.slice(0, 10)))
  } catch {
    /* ignore */
  }
}

function renderSent() {
  if (!sentRequests) return
  const items = loadSent()
  if (!items.length) {
    sentRequests.hidden = true
    return
  }

  const title =
    currentLang === 'de' ? 'Gesendete Anfragen (dieser Browser)' : 'Sent requests (this browser)'
  const lines = items.slice(0, 5).map((it) => {
    const when = typeof it.createdAt === 'string' ? new Date(it.createdAt) : null
    const whenText = when && !Number.isNaN(when.getTime()) ? when.toLocaleString() : ''
    const name = typeof it.name === 'string' ? it.name : ''
    const email = typeof it.email === 'string' ? it.email : ''
    const sep1 = whenText ? ' — ' : ''
    const sep2 = name ? ' · ' : ''
    return `<li>${whenText}${sep1}${name}${sep2}${email}</li>`
  })

  sentRequests.innerHTML = `<h3>${title}</h3><ul>${lines.join('')}</ul>`
  sentRequests.hidden = false
}

async function submitContact(payload) {
  const res = await fetch('/api/contact', {
    method: 'POST',
    headers: {
      'content-type': 'application/json',
    },
    body: JSON.stringify(payload),
  })

  if (!res.ok) {
    throw new Error('CONTACT_SUBMIT_FAILED')
  }
}

form?.addEventListener('submit', async (e) => {
  e.preventDefault()
  const privacy = form.querySelector('#privacy')
  if (!form.checkValidity() || !privacy?.checked) {
    form.reportValidity()
    return
  }

  const data = new FormData(form)
  const payload = {
    name: String(data.get('name') || ''),
    email: String(data.get('email') || ''),
    plz: String(data.get('plz') || ''),
    message: String(data.get('message') || ''),
  }

  const submitBtn = form.querySelector('button[type="submit"]')
  const prevDisabled = submitBtn?.disabled
  if (submitBtn) submitBtn.disabled = true

  try {
    await submitContact(payload)
    showToast(STRINGS[currentLang]['toast.sent'])
    const next = [{ ...payload, createdAt: new Date().toISOString() }, ...loadSent()]
    saveSent(next)
    renderSent()
    form.reset()
  } catch {
    showToast(currentLang === 'de' ? 'Senden fehlgeschlagen. Bitte später erneut versuchen.' : 'Sending failed. Please try again later.')
  } finally {
    if (submitBtn) submitBtn.disabled = Boolean(prevDisabled)
  }
})

